public class MathClass {
    public static void main(String[] args) {
        System.out.println(Math.min(100, 200));
        System.out.println(Math.max(100,200));
        System.out.println(Math.abs(-12));
        System.out.println(Math.sqrt(36));
        System.out.println(Math.pow(3, 3));
        System.out.println(Math.E);
        System.out.println(Math.PI);
        System.out.println(Math.TAU);
        System.out.println(Math.IEEEremainder(1.2,2.1));
        System.out.println(Math.abs(123.321));
        System.out.println(Math.acos(123.1));
        System.out.println(Math.cbrt(2.0));
        System.out.println(Math.ceil(6.9));
        System.out.println(Math.floor(1.1));
        System.out.println(Math.sin(90));
        System.out.println(Math.cos(90));
        System.out.println(Math.tan(90));
        System.out.println(Math.sinh(90));
        System.out.println(Math.cosh(90));
        System.out.println(Math.tanh(90));
        System.out.println(Math.decrementExact(10));
        System.out.println(Math.incrementExact(10));
        System.out.println(Math.getExponent(123.456));
        System.out.println(Math.floorDiv(50, 9));
        System.out.println(Math.exp(1));
        System.out.println(Math.log10(0100));
        System.out.println(Math.toRadians(123));
        System.out.println(Math.toDegrees(Math.atan(1)));
        System.out.println(Math.random()*100);
        System.out.println(Math.multiplyExact(1000, 200));
        System.out.println(Math.nextAfter(12.3, 20));

        //StrictMath class
        System.out.println(StrictMath.abs(112.3));
    }
}
